    return  Mamifero.__str__(self) + "\n" + AnimalMarino.__str__(self) + f"\nEste Cetaceo puede medir mas de {self.__notas} metros, vive en el {self.__viveEn} y su peso puede ser hasta de {self.__peso} toneladas." 

